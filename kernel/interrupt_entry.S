[bits 32]
%define COMPENSATE_ERROR_CODE push 0
%define NO_COMPENSATE nop
extern intr_handler_table

section .data
global intr_entry_table
intr_entry_table:

%macro VECTOR 2
section .text
intr%1entry:
    %2
    push ds
    push es
    push fs
    push gs
    pushad
    
    push %1     ;save int vector
    call [intr_handler_table + %1*4]      ;call intr handler function
    jmp intr_exit
    
section .data
    dd intr%1entry      ;save intr entry addr

%endmacro

section .text
global intr_exit
intr_exit:
    add esp,4   ;vector
    popad
    pop gs
    pop fs
    pop es
    pop ds
    add esp,4   ;error code/compensation
    iretd

